package com.example.mobileterm.BulletinBoard;

public class TagDocs {
    String did;
    String tag;

    public TagDocs(String did, String tag) {
        this.did = did;
        this.tag = tag;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getDid() {
        return did;
    }

    public void setDid(String did) {
        this.did = did;
    }


}
